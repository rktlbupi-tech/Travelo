        <script>
            // ===== CAB CITY DROPDOWN =====
            const cabCities = [
                { city: "Delhi", code: "DEL", airport: "Indira Gandhi International Airport" },
                { city: "Mumbai", code: "BOM", airport: "Chhatrapati Shivaji International Airport" },
                { city: "Bangalore", code: "BLR", airport: "Kempegowda International Airport" }
            ];

            function closeAllCabDropdowns() {
                document.querySelectorAll(".cab-dropdown").forEach(d => d.classList.remove("show"));
            }

            document.querySelectorAll(".cab-wrapper").forEach(wrapper => {
                const input = wrapper.querySelector(".cab-input");
                const display = wrapper.querySelector(".cab-display");
                const dropdown = wrapper.querySelector(".cab-dropdown");

                function showCabDropdown(filteredCities) {
                    dropdown.innerHTML = "";
                    filteredCities.forEach(c => {
                        const btn = document.createElement("button");
                        btn.type = "button";
                        btn.className = "dropdown-item";
                        btn.innerHTML = `
        <strong>${c.city} (${c.code})</strong>
        <div class="small text-muted">${c.airport}</div>
      `;
                        btn.onclick = () => {
                            input.value = `${c.city} (${c.code})`;
                            display.innerHTML = `
          <div class="cab-name">${c.city}</div>
          <div class="cab-airport">[${c.code}] ${c.airport}</div>
        `;
                            display.style.display = "block";
                            input.style.color = "transparent";
                            closeAllCabDropdowns();
                        };
                        dropdown.appendChild(btn);
                    });
                    dropdown.classList.toggle("show", filteredCities.length > 0);
                }

                input.addEventListener("focus", () => showCabDropdown(cabCities));

                input.addEventListener("input", () => {
                    input.style.color = "#212529"; // restore color
                    const query = input.value.toLowerCase();
                    const filtered = cabCities.filter(c =>
                        c.city.toLowerCase().includes(query) ||
                        c.code.toLowerCase().includes(query) ||
                        c.airport.toLowerCase().includes(query)
                    );
                    showCabDropdown(filtered);
                });

                display.addEventListener("click", () => {
                    display.style.display = "none";
                    input.style.color = "#212529";
                    input.focus();
                });
            });

            // ===== DATE/TIME PICKER SETUP =====
            function setupDropdown(triggerId, dropdownId, dateInputId, hourId, minuteId, ampmId, dateDisplayId, timeDisplayId) {
                const trigger = document.getElementById(triggerId);
                const dropdown = document.getElementById(dropdownId);
                const dateInput = document.getElementById(dateInputId);
                const hourInput = document.getElementById(hourId);
                const minuteInput = document.getElementById(minuteId);
                const ampmSelect = document.getElementById(ampmId);
                const dateDisplay = document.getElementById(dateDisplayId);
                const timeDisplay = document.getElementById(timeDisplayId);
                const setBtn = dropdown.querySelector('button');

                trigger.addEventListener('click', e => {
                    e.stopPropagation();
                    dropdown.classList.toggle('show');
                });

                dropdown.addEventListener('click', e => e.stopPropagation());

                setBtn.addEventListener('click', e => {
                    e.stopPropagation();
                    if (!dateInput.value) return;
                    dateDisplay.textContent = dateInput.value;
                    const timeStr = `${hourInput.value || '00'}:${minuteInput.value || '00'} ${ampmSelect.value}`;
                    timeDisplay.textContent = timeStr;
                    dropdown.classList.remove('show');

                    if (dateInputId === 'pickupDateInput') {
                        document.getElementById('hiddenPickupDate').value = dateInput.value;
                        document.getElementById('hiddenPickupTime').value = timeStr;
                    } else if (dateInputId === 'returnDateInput') {
                        document.getElementById('hiddenReturnDate').value = dateInput.value;
                        document.getElementById('hiddenReturnTime').value = timeStr;
                    }
                });
            }

            setupDropdown('pickupTrigger', 'pickupDropdown', 'pickupDateInput', 'hourInput', 'minuteInput', 'ampmSelect', 'pickupDate', 'pickupTime');
            setupDropdown('returnTrigger', 'returnDropdown', 'returnDateInput', 'returnHourInput', 'returnMinuteInput', 'returnAmpmSelect', 'returnDate', 'returnTime');

            // ===== CLICK OUTSIDE TO CLOSE DROPDOWNS =====
            document.addEventListener('click', e => {
                // Cab dropdowns
                if (!e.target.closest(".cab-wrapper")) closeAllCabDropdowns();
                // Date/time dropdowns
                ['pickupDropdown', 'returnDropdown'].forEach(id => {
                    const dropdown = document.getElementById(id);
                    const trigger = document.getElementById(id.replace('Dropdown', 'Trigger'));
                    if (dropdown.classList.contains('show') && !dropdown.contains(e.target) && !trigger.contains(e.target)) {
                        dropdown.classList.remove('show');
                    }
                });
            });

            // ===== TRIP TYPE TOGGLE =====
            const tripRadios = document.querySelectorAll('input[name="tripType"]');
            const pickupTypeWrapper = document.getElementById('pickupTypeWrapper');
            const fromWrapper = document.getElementById('fromWrapper');
            const toWrapper = document.getElementById('toWrapper');
            const pickupWrapper = document.getElementById('pickupWrapper');
            const returnWrapper = document.getElementById('returnWrapper');
            const hourlyWrapper = document.getElementById('hourlyWrapper');

            function updateCabForm() {
                const tripType = document.querySelector('input[name="tripType"]:checked').value;
                // hide all first
                pickupTypeWrapper.classList.add('d-none');
                fromWrapper.classList.add('d-none');
                toWrapper.classList.add('d-none');
                pickupWrapper.classList.add('d-none');
                returnWrapper.classList.add('d-none');
                hourlyWrapper.classList.add('d-none');

                if (tripType === 'Transfer') {
                    pickupTypeWrapper.classList.remove('d-none');
                    fromWrapper.classList.remove('d-none');
                    toWrapper.classList.remove('d-none');
                    pickupWrapper.classList.remove('d-none');
                }
                if (tripType === 'Outstation') {
                    fromWrapper.classList.remove('d-none');
                    toWrapper.classList.remove('d-none');
                    pickupWrapper.classList.remove('d-none');
                    returnWrapper.classList.remove('d-none');
                }
                if (tripType === 'Hourly') {
                    pickupTypeWrapper.classList.remove('d-none');
                    fromWrapper.classList.remove('d-none');
                    pickupWrapper.classList.remove('d-none');
                    hourlyWrapper.classList.remove('d-none');
                }
            }

            tripRadios.forEach(r => r.addEventListener('change', updateCabForm));
            updateCabForm(); // initial



            document.getElementById('cabSearchForm').addEventListener('submit', function (e) {
                e.preventDefault();

                const formData = new FormData(this);
                const tripType = formData.get('tripType');
                const from = formData.get('from');
                const to = formData.get('to');
                const pickupDate = formData.get('pickup_date');
                const pickupTime = formData.get('pickup_time');
                const returnDate = formData.get('return_date');
                const hours = formData.get('hours');

                if (tripType !== 'Hourly' && from && to && from.trim() === to.trim()) {
                    Swal.fire({
                        icon: 'error',
                        title: 'Invalid Selection',
                        text: 'Pickup and drop regions cannot be the same!'
                    });
                    return;
                }

                // Ensure 'from' is filled
                if (!from || from.trim() === '') {
                    Swal.fire({ icon: 'error', title: 'Missing Field', text: 'Please select a pickup location.' });
                    return;
                }

                // Ensure pickup date and time are set
                if (!pickupDate || !pickupTime) {
                    Swal.fire({ icon: 'error', title: 'Missing Field', text: 'Please select a pickup date and time.' });
                    return;
                }

                if (tripType === 'Transfer') {
                    // no additional checks needed for to? wait, maybe TO is required
                    if (!to || to.trim() === '') {
                        Swal.fire({ icon: 'error', title: 'Missing Field', text: 'Please select a drop location.' });
                        return;
                    }
                } else if (tripType === 'Outstation') {
                    if (!to || to.trim() === '') {
                        Swal.fire({ icon: 'error', title: 'Missing Field', text: 'Please select a drop location.' });
                        return;
                    }
                    if (!returnDate) {
                        Swal.fire({ icon: 'error', title: 'Missing Field', text: 'Please select a return date and time for Outstation.' });
                        return;
                    }
                    if (new Date(returnDate) < new Date(pickupDate)) {
                        Swal.fire({ icon: 'error', title: 'Invalid Dates', text: 'Return date cannot be earlier than pickup date!' });
                        return;
                    }
                } else if (tripType === 'Hourly') {
                    if (!hours) {
                        Swal.fire({ icon: 'error', title: 'Missing Field', text: 'Please select the number of hours.' });
                        return;
                    }
                }

                Swal.fire({
                    title: 'Processing Booking...',
                    text: 'Please wait while we submit your cab request.',
                    allowOutsideClick: false,
                    didOpen: () => {
                        Swal.showLoading();
                    }
                });

